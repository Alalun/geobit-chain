package io.geobit.chain.entity.biteasy;

import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
public class BitEasyResponse {
	private Integer status;
	private BitEasyAddressData Data;
	public Integer getStatus() {
		return status;
	}
	public void setStatus(Integer status) {
		this.status = status;
	}
	public BitEasyAddressData getData() {
		return Data;
	}
	public void setData(BitEasyAddressData data) {
		Data = data;
	}
	@Override
	public String toString() {
		return "BitEasyResponse [status=" + status + ", Data=" + Data + "]";
	}

	
	
}
